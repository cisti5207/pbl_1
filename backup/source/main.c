void trim(char *_string)
{
    int i = strlen(_string) - 1;

    while (i >= 0 && (_string[i] == ' ' || _string[i] == '\n' || _string[i] == '\t'))
    {
        _string[i] = 0;
        i--;
    }

    if (i == 0) return;

    i = 0;
    while (_string[i] == ' ' || _string[i] == '\t') i++;

    for (int j = 0; j + i <= strlen(_string); j++)
        _string[j] = _string[i + j];
}


